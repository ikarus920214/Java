
public class SwitchCase2 {

	public static void main(String[] args) {
		String medal = "전진";
		
		switch(medal) {
		case "Gold":
			System.out.println("골딱입니다.");
		break;
		case "Silver":
			System.out.println("실딱입니다.");
		break;
		case "Bronze":
			System.out.println("브딱입니다.");
		break;
		case "Iron":
			System.out.println("아이언딱입니다.");
		break;
		default:
			System.out.println("언랭입니다.");
		break;
			
		}

	}

}
